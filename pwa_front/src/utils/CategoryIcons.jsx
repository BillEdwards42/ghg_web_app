export const getIconForCategoryMenu = (layerIndex, rootCategoryName) => {
  if (layerIndex === 0) return CategoryIcons.grid;
  if (layerIndex === 2) return CategoryIcons.cog;

  // Layer 1 (Subcategories) - determine icon based on root category naming
  if (rootCategoryName.includes('類別一')) return CategoryIcons.plant;
  if (rootCategoryName.includes('類別二')) return CategoryIcons.power;
  if (rootCategoryName.includes('類別三')) return CategoryIcons.car;
  if (rootCategoryName.includes('類別四')) return CategoryIcons.supplyChain;
  if (rootCategoryName.includes('類別五')) return CategoryIcons.franchise;
  if (rootCategoryName.includes('類別六')) return CategoryIcons.other;

  return CategoryIcons.default;
};
